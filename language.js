function changeLanguage(lang) {
  const elements = document.querySelectorAll("[data-key]");

  elements.forEach((el) => {
    const key = el.getAttribute("data-key");

    if (key.startsWith("how_step")) return;

    // Traducción normal
    const value = translations?.[lang]?.[key];
    if (value !== undefined) {
      const tag = el.tagName.toLowerCase();
      if (tag === "input" || tag === "textarea") {
        el.placeholder = value;
      } else {
        el.innerHTML = value;
      }
    }
  });

  // Actualizar <html lang="...">
  document.documentElement.lang = lang;
  localStorage.setItem("language", lang);

  // Actualizar texto del botón del toggle
  const btn = document.querySelector(".lang-toggle span:last-child");
  if (btn) btn.textContent = lang.toUpperCase();

  // Actualizar <title>
  const titleEl = document.querySelector("title[data-key]");
  if (titleEl) {
    const titleKey = titleEl.getAttribute("data-key");
    const titleVal = translations?.[lang]?.[titleKey];
    if (titleVal) titleEl.innerHTML = titleVal;
  }

  // 🔄 IMPORTANTE:
  // Actualizar también HOW (esto lo maneja tu otro script)
  if (typeof updateHowContent === "function") {
    setTimeout(() => {
      updateHowContent(window.currentSegment || "paciente");
    }, 0);
  }
}

document.addEventListener("DOMContentLoaded", () => {
  // Detectar idioma guardado o navegador
  const saved = localStorage.getItem("language");

  const initial =
    saved ||
    ((navigator.language || navigator.userLanguage || "es").toLowerCase().startsWith("en")
      ? "en"
      : "es");

  // Aplicar idioma inicial
  changeLanguage(initial);

  // Toggle para cambiar entre ES / EN
  const toggle = document.querySelector(".lang-toggle");
  if (toggle) {
    toggle.addEventListener("click", () => {
      const curr = localStorage.getItem("language") || initial;
      const next = curr === "es" ? "en" : "es";
      changeLanguage(next);
    });
  }
});
