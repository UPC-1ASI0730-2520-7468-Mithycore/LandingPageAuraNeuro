function getCurrentPageKey() {
  const path = window.location.pathname.toLowerCase();
  if (path.includes("privacidad")) return "privacy";
  if (path.includes("terminos")) return "terms";
  return "home";
}

function setPageTitle(lang, pageKey) {
  if (pageKey === "privacy") {
    document.title = translations[lang].page_title_privacy;
  } else if (pageKey === "terms") {
    document.title = translations[lang].page_title_terms;
  } else {
    document.title = translations[lang].page_title_home;
  }
}

function setTextByKey(lang) {
  document.querySelectorAll("[data-key]").forEach((el) => {
    const key = el.dataset.key;
    const value = translations[lang][key];
    if (!value) return;

    if (value.includes("<br>") || value.includes("<strong>") || value.includes("<a ")) {
      el.innerHTML = value;
    } else {
      el.textContent = value;
    }
  });
}

function updateLangButton(lang) {
  const btn = document.querySelector(".lang-toggle");
  if (!btn) return;
  const span = btn.querySelector(".lang-label");
  if (span) span.textContent = lang.toUpperCase();
  btn.dataset.lang = lang;
  btn.setAttribute("aria-label", lang === "es" ? "Cambiar idioma a inglés" : "Switch language to Spanish");
  btn.setAttribute("title", lang === "es" ? "Idioma: Español" : "Language: English");
}

function getHowSegmentByButton(button) {
  if (!button) return "jugador";
  const target = button.getAttribute("data-target");
  return target || "jugador";
}

function normalizeHowKey(lang, segment) {
  if (lang === "en") {
    if (segment === "jugador") return "player";
    if (segment === "entrenador") return "coach";
  }
  return segment;
}

function updateHowSection(lang) {
  const activeBtn = document.querySelector(".how-icon.active") || document.querySelector('.how-icon[data-target="jugador"]');
  const rawSegment = getHowSegmentByButton(activeBtn);
  const segment = normalizeHowKey(lang, rawSegment);
  const data = howTranslations[lang]?.[segment];

  if (!data) return;

  const s1t = document.getElementById("how-step1-title");
  const s1x = document.getElementById("how-step1-text");
  const s2t = document.getElementById("how-step2-title");
  const s2x = document.getElementById("how-step2-text");
  const s3t = document.getElementById("how-step3-title");
  const s3x = document.getElementById("how-step3-text");

  if (s1t) s1t.textContent = data.step1.title;
  if (s1x) s1x.textContent = data.step1.text;
  if (s2t) s2t.textContent = data.step2.title;
  if (s2x) s2x.textContent = data.step2.text;
  if (s3t) s3t.textContent = data.step3.title;
  if (s3x) s3x.textContent = data.step3.text;
}

function applyLanguage(lang) {
  const pageKey = getCurrentPageKey();
  document.documentElement.lang = lang;
  localStorage.setItem("courtly-language", lang);
  setPageTitle(lang, pageKey);
  setTextByKey(lang);
  updateLangButton(lang);
  updateHowSection(lang);
}

function attachHowLanguageSync() {
  const howButtons = document.querySelectorAll(".how-icon");
  if (!howButtons.length) return;

  howButtons.forEach((btn) => {
    btn.addEventListener("click", () => {
      const currentLang = localStorage.getItem("courtly-language") || "es";
      setTimeout(() => updateHowSection(currentLang), 0);
    });
  });
}

document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("courtly-language") || "es";
  applyLanguage(savedLang);

  const langBtn = document.querySelector(".lang-toggle");
  if (langBtn) {
    langBtn.addEventListener("click", () => {
      const currentLang = document.documentElement.lang === "en" ? "en" : "es";
      const nextLang = currentLang === "es" ? "en" : "es";
      applyLanguage(nextLang);
    });
  }

  attachHowLanguageSync();
});
